/*

-Declarar um StringBuilder:

 StringBuilder frase = new  StringBuilder(10);

 entro os '()' da class StringBuilder se passa o tamanho da string que se quer criar.

-Para adicionar uma strings na StringBuilder usa-se o método 'append("");'. Exemplo de uso:

 StringBuilder sb = new StringBuilder(14);
  sb.append("Eduardo");

-Métodos úteis da class StringBuilder:

1- .reverse(""); Inverte toda a string;

2- .delete(0,2); apaga caracteres das string, o primeiro argumento conta a string com o primeiro índice
   valendo 0, e o segundo argumento conta a string com o primeiro índice valendo 1.

3- .insert(2,"palavra"); adiciona uma string ou um caractere no índice que vc passar no primeiro argumento,
    esse método começa a contar a string que terá com o primeiro índice valendo 1.

Anotação:
As class StringBuilder e StringBuffer têm os mesmos métodos, mas a class StringBuffer é usada em ambientes
multi-thread, e por esse motivo seus métodos são sincronizados.

 */

